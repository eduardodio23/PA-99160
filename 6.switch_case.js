// Switch - case

const opcao = 3

switch (opcao) {
    case 1:
        console.log("opção 1")
        break;
    case 2:
        console.log("opção 2")
        break;
    case 3:
        console.log("opção 3")
        break;
    case 4:
        console.log("opção 4")
        break;
    
    case 5:
        console.log("opção 5")
        break;
    default:
        console.log("Opção inválida")
        break;
}