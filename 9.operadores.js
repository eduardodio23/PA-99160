//Operadores logicos

let idade1 = 10
let idade2 = "10"

console.log("\n==comparação conteúdo==")
console.log(`A variável ${idade1} é igual a ${idade2}`)
// só compara o conteudo 
console.log(idade1 == idade2)


console.log("\n==comparação tipo e conteúdo==")
console.log(`A variável ${idade1} é igual a ${idade2}`)
//saber se são difentes
console.log(idade1 != idade2)

console.log("\n==comparação tipo e conteúdo==")
console.log(`A variável ${idade1} é igual a ${idade2}`)
// se tipo e conteudo é igual
console.log(idade1 === idade2)

console.log("\n==comparação tipo e conteúdo==")
console.log(`A variável ${idade1} é igual a ${idade2}`)
// se eles são estritamentes diferentes
console.log(idade1 !== idade2)

console.log("\n== AND ==")
console.log("1 < 2 && 1 < 3")
console.log(1 < 2 && 1 < 3)

console.log("\n== or ==")
console.log("10 < 2 || 1 < 3")
console.log(10 < 2 || 1 < 3)

